# 3. ok_plot_segments() - Longitudinal profile
# =============================================================================

#' Plot a longitudinal water quality profile across reservoir segments
#'
#' @description
#' \code{ok_plot_segments()} visualises the spatial gradient in water quality
#' from riverine to lacustrine zones of a multi-segment reservoir, using the
#' output of \code{ok_segment_summary()} or \code{ok_segment_chain()}.
#'
#' @param segment_data Either a named list of \code{okBATHTUB} objects from
#'   \code{ok_segment_chain()}, or a data frame from \code{ok_segment_summary()}.
#' @param metrics Character vector. Which metrics to plot. Any subset of
#'   \code{c("tp_inlake_ugl", "chla_ugl", "secchi_m", "tsi_mean",
#'   "tp_retention_pct")}. Default: first four.
#' @param lake_name Character. Lake name for the plot title.
#'
#' @return A \code{ggplot2} object.
#'
#' @examples
#' \dontrun{
#' segs <- list(
#'   list(label = "Riverine",     surface_area_ha = 280, mean_depth_m = 3.1),
#'   list(label = "Transitional", surface_area_ha = 410, mean_depth_m = 4.5),
#'   list(label = "Lacustrine",   surface_area_ha = 610, mean_depth_m = 5.8)
#' )
#' chain <- ok_segment_chain(45e6, 150, tn_inflow_ugl = 2200, segments = segs)
#' ok_plot_segments(chain, lake_name = "Example Reservoir")
#' }
#'
#' @seealso \code{\link{ok_segment_chain}}, \code{\link{ok_segment_summary}}
#' @export

ok_plot_segments <- function(segment_data,
                              metrics    = c("tp_inlake_ugl", "chla_ugl",
                                             "secchi_m", "tsi_mean"),
                              lake_name  = NULL) {

  if (!requireNamespace("ggplot2", quietly = TRUE))
    stop("Package 'ggplot2' is required.", call. = FALSE)
  if (!requireNamespace("tidyr", quietly = TRUE))
    stop("Package 'tidyr' is required.", call. = FALSE)
  if (!requireNamespace("dplyr", quietly = TRUE))
    stop("Package 'dplyr' is required.", call. = FALSE)

  # Accept either a chain list or a summary data frame
  if (is.list(segment_data) && !is.data.frame(segment_data)) {
    df <- ok_segment_summary(segment_data)
  } else if (is.data.frame(segment_data)) {
    df <- segment_data
  } else {
    stop("'segment_data' must be a list from ok_segment_chain() or a data frame from ok_segment_summary().",
         call. = FALSE)
  }

  metric_labels <- c(
    tp_inlake_ugl    = "In-Lake TP (ug/L)",
    chla_ugl         = "Chlorophyll-a (ug/L)",
    secchi_m         = "Secchi Depth (m)",
    tsi_mean         = "Mean Carlson TSI",
    tp_retention_pct = "TP Retention (%)"
  )

  metrics <- intersect(metrics, names(df))
  metrics <- intersect(metrics, names(metric_labels))

  if (length(metrics) == 0)
    stop("None of the requested metrics found in segment data.", call. = FALSE)

  df$segment <- factor(df$segment, levels = df$segment)

  long_df <- df |>
    dplyr::select(segment, dplyr::all_of(metrics)) |>
    tidyr::pivot_longer(
      cols      = dplyr::all_of(metrics),
      names_to  = "metric",
      values_to = "value"
    ) |>
    dplyr::mutate(
      metric_label = metric_labels[metric],
      metric_label = factor(metric_label, levels = metric_labels[metrics])
    )

  title_str <- if (!is.null(lake_name)) {
    sprintf("%s \u2014 Longitudinal Profile", lake_name)
  } else {
    "Longitudinal Segment Profile"
  }

  ggplot2::ggplot(long_df,
                   ggplot2::aes(x = segment, y = value, group = 1)) +
    ggplot2::geom_line(color = "#1B5E8A", linewidth = 1.0) +
    ggplot2::geom_point(color = "#1B5E8A", fill = "white",
                         shape = 21, size = 3.5, stroke = 1.5) +
    ggplot2::facet_wrap(~ metric_label, scales = "free_y", ncol = 2) +
    ggplot2::labs(
      title    = title_str,
      subtitle = "Riverine \u2192 Lacustrine gradient (left to right)",
      x        = NULL,
      y        = NULL,
      caption  = "okBATHTUB R package"
    ) +
    ok_theme()
}


# =============================================================================
# 4. ok_plot_tsi() - Carlson TSI diagram
# =============================================================================

#' Plot a Carlson TSI deviation diagram
#'
#' @description
#' \code{ok_plot_tsi()} produces a Carlson (1977) TSI deviation diagram,
#' plotting TSI(TP) on the x-axis against TSI(Chl-a) and TSI(Secchi) on
#' the y-axis with trophic state background bands. Deviations from the
#' 1:1 line indicate light limitation (Chl-a below TP line) or non-algal
#' turbidity (Secchi below Chl-a line).
#'
#' Can accept either a single \code{okBATHTUB} result, or a data frame
#' containing the required TSI columns (e.g. from \code{ok_scenario()}
#' or a user-supplied observed-data summary).
#'
#' @param x An \code{okBATHTUB} object, or a data frame containing columns
#'   \code{tsi_tp}, \code{tsi_chla}, and optionally \code{tsi_secchi}.
#'   If a data frame, it may also contain \code{lake_name} and
#'   \code{eco_l3_name} for grouping.
#' @param color_by Character. Column name in a data frame to color points by.
#'   Common choices: \code{"eco_l3_name"}, \code{"trophic_state"},
#'   \code{"lake_name"}. Ignored when \code{x} is an \code{okBATHTUB} object.
#' @param lake_name Character. Lake name for the plot title when \code{x}
#'   is an \code{okBATHTUB} object.
#' @param show_diagonal Logical. Show the 1:1 reference line. Default
#'   \code{TRUE}.
#'
#' @return A \code{ggplot2} object.
#'
#' @examples
#' \dontrun{
#' # From a single pipeline result
#' result <- ok_load(inflow_m3yr = 45e6, tp_inflow_ugl = 120,
#'                    coefficients = "oklahoma",
#'                    ecoregion    = "Cross Timbers") |>
#'   ok_hydraulics(surface_area_ha = 890, mean_depth_m = 4.2) |>
#'   ok_retention() |>
#'   ok_inlake()    |>
#'   ok_tsi()
#' ok_plot_tsi(result, lake_name = "Arcadia Lake")
#'
#' # From observed AWQMS data
#' obs_tsi <- all_lakes |>
#'   dplyr::filter(!is.na(tp_ugl), !is.na(chla_ugl), !is.na(secchi_m)) |>
#'   dplyr::rowwise() |>
#'   dplyr::mutate(
#'     tsi_tp     = 14.42 * log(tp_ugl)   + 4.15,
#'     tsi_chla   = 9.81  * log(chla_ugl) + 30.6,
#'     tsi_secchi = 60.0  - 14.41 * log(secchi_m)
#'   )
#' ok_plot_tsi(obs_tsi, color_by = "eco_l3_name")
#' }
#'
#' @references
#' Carlson, R.E. (1977). A trophic state index for lakes.
#' \emph{Limnology and Oceanography}, 22(2), 361-369.
#'
#' @seealso \code{\link{ok_tsi}}, \code{\link{ok_tsi_observed}}
#' @export

ok_plot_tsi <- function(x,
                         color_by      = NULL,
                         lake_name     = NULL,
                         show_diagonal = TRUE) {

  if (!requireNamespace("ggplot2", quietly = TRUE))
    stop("Package 'ggplot2' is required.", call. = FALSE)
  if (!requireNamespace("tidyr", quietly = TRUE))
    stop("Package 'tidyr' is required.", call. = FALSE)
  if (!requireNamespace("dplyr", quietly = TRUE))
    stop("Package 'dplyr' is required.", call. = FALSE)

  pal    <- ok_trophic_colors()

  # Trophic state band data
  bands <- data.frame(
    xmin  = c(0,  40, 50, 70),
    xmax  = c(40, 50, 70, 100),
    ymin  = c(0,  40, 50, 70),
    ymax  = c(40, 50, 70, 100),
    state = c("Oligotrophic", "Mesotrophic", "Eutrophic", "Hypereutrophic")
  )

  # --- Build plot data from okBATHTUB object ---
  if (inherits(x, "okBATHTUB")) {

    d <- x$data
    plot_df <- data.frame(
      tsi_tp     = d$tsi_tp     %||% NA_real_,
      tsi_chla   = d$tsi_chla   %||% NA_real_,
      tsi_secchi = d$tsi_secchi %||% NA_real_,
      state      = d$trophic_state %||% NA_character_
    )

    title_str <- if (!is.null(lake_name)) {
      sprintf("%s \u2014 Carlson TSI Diagram", lake_name)
    } else {
      "Carlson TSI Diagram"
    }

    # Pivot to long for Chl-a and Secchi lines
    long_df <- tidyr::pivot_longer(
      plot_df,
      cols      = c(tsi_chla, tsi_secchi),
      names_to  = "tsi_type",
      values_to = "tsi_y"
    ) |>
    dplyr::mutate(
      tsi_type = dplyr::recode(tsi_type,
                                "tsi_chla"   = "TSI(Chl-a)",
                                "tsi_secchi" = "TSI(Secchi)")
    ) |>
    dplyr::filter(!is.na(tsi_y), !is.na(tsi_tp))

    p <- ggplot2::ggplot(long_df,
                          ggplot2::aes(x = tsi_tp, y = tsi_y,
                                       color = tsi_type, shape = tsi_type)) +
      ggplot2::geom_rect(
        data        = bands,
        ggplot2::aes(xmin = xmin, xmax = xmax,
                     ymin = ymin, ymax = ymax, fill = state),
        alpha       = 0.10,
        inherit.aes = FALSE
      ) +
      ggplot2::scale_fill_manual(values = pal, name = "Trophic State",
                                  guide = ggplot2::guide_legend(order = 2)) +
      ggplot2::geom_point(size = 4) +
      ggplot2::scale_color_manual(
        values = c("TSI(Chl-a)"   = "#1B5E8A",
                   "TSI(Secchi)"  = "#D9534F"),
        name   = NULL,
        guide  = ggplot2::guide_legend(order = 1)
      ) +
      ggplot2::scale_shape_manual(
        values = c("TSI(Chl-a)" = 16, "TSI(Secchi)" = 17),
        name   = NULL,
        guide  = ggplot2::guide_legend(order = 1)
      )

  } else if (is.data.frame(x)) {

    # --- Build plot data from data frame ---
    required <- c("tsi_tp", "tsi_chla")
    missing  <- setdiff(required, names(x))
    if (length(missing) > 0)
      stop(sprintf("Data frame missing required columns: %s",
                   paste(missing, collapse = ", ")), call. = FALSE)

    title_str <- if (!is.null(lake_name)) {
      sprintf("%s \u2014 Carlson TSI Diagram", lake_name)
    } else {
      "Carlson TSI Diagram"
    }

    plot_df <- x[!is.na(x$tsi_tp) & !is.na(x$tsi_chla), ]

    aes_map <- if (!is.null(color_by) && color_by %in% names(plot_df)) {
      ggplot2::aes(x = tsi_tp, y = tsi_chla,
                   color = .data[[color_by]])
    } else {
      ggplot2::aes(x = tsi_tp, y = tsi_chla)
    }

    p <- ggplot2::ggplot(plot_df, aes_map) +
      ggplot2::geom_rect(
        data        = bands,
        ggplot2::aes(xmin = xmin, xmax = xmax,
                     ymin = ymin, ymax = ymax, fill = state),
        alpha       = 0.10,
        inherit.aes = FALSE
      ) +
      ggplot2::scale_fill_manual(values = pal, name = "Trophic State",
                                  guide = ggplot2::guide_legend(order = 2)) +
      ggplot2::geom_point(alpha = 0.7, size = 2.5)

    if (is.null(color_by)) {
      p <- p + ggplot2::scale_color_manual(values = "#1B5E8A", guide = "none")
    }

  } else {
    stop("'x' must be an okBATHTUB object or a data frame.", call. = FALSE)
  }

  # 1:1 diagonal reference line
  if (show_diagonal) {
    p <- p +
      ggplot2::geom_abline(slope = 1, intercept = 0,
                            linetype = "dashed",
                            color    = "grey40",
                            linewidth = 0.6)
  }

  p <- p +
    ggplot2::coord_fixed(ratio = 1, xlim = c(0, 100), ylim = c(0, 100)) +
    ggplot2::labs(
      title    = title_str,
      subtitle = "Points above 1:1 line: algae-limited clarity | Below: non-algal turbidity",
      x        = "TSI(TP)",
      y        = if (inherits(x, "okBATHTUB")) "TSI(Chl-a) / TSI(Secchi)" else "TSI(Chl-a)",
      caption  = "okBATHTUB R package"
    ) +
    ok_theme()

  p
}
