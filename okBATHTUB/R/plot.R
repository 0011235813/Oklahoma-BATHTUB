# 2. ok_plot_scenario() - Scenario comparison
# =============================================================================

#' Plot scenario comparison from ok_scenario() output
#'
#' @description
#' \code{ok_plot_scenario()} visualises the output of \code{ok_scenario()} or
#' \code{ok_scenario_sweep()} as a faceted dot-and-line chart, showing how
#' key water quality metrics change across loading scenarios.
#'
#' @param scenario_result A data frame returned by \code{ok_scenario()} or
#'   \code{ok_scenario_sweep()}.
#' @param metrics Character vector. Which metrics to plot. Any subset of
#'   \code{c("tp_inlake_ugl", "chla_ugl", "secchi_m", "tsi_mean")}.
#'   Default: all four.
#' @param highlight_target Logical. If the scenario result contains a
#'   \code{meets_target} column, highlight scenarios that meet the target
#'   in green. Default \code{TRUE}.
#' @param lake_name Character. Lake name for the plot title.
#'
#' @return A \code{ggplot2} object.
#'
#' @examples
#' \dontrun{
#' baseline <- ok_load(inflow_m3yr = 45e6, tp_inflow_ugl = 120,
#'                      coefficients = "oklahoma",
#'                      ecoregion    = "Cross Timbers") |>
#'   ok_hydraulics(surface_area_ha = 890, mean_depth_m = 4.2)
#'
#' sweep <- ok_scenario_sweep(baseline, target_class = "mesotrophic")
#' ok_plot_scenario(sweep, lake_name = "Arcadia Lake")
#' }
#'
#' @seealso \code{\link{ok_scenario}}, \code{\link{ok_scenario_sweep}}
#' @export

ok_plot_scenario <- function(scenario_result,
                              metrics          = c("tp_inlake_ugl", "chla_ugl",
                                                   "secchi_m", "tsi_mean"),
                              highlight_target = TRUE,
                              lake_name        = NULL) {

  if (!requireNamespace("ggplot2", quietly = TRUE))
    stop("Package 'ggplot2' is required.", call. = FALSE)
  if (!requireNamespace("tidyr", quietly = TRUE))
    stop("Package 'tidyr' is required.", call. = FALSE)
  if (!requireNamespace("dplyr", quietly = TRUE))
    stop("Package 'dplyr' is required.", call. = FALSE)

  if (!is.data.frame(scenario_result))
    stop("'scenario_result' must be a data frame from ok_scenario().",
         call. = FALSE)

  metric_labels <- c(
    tp_inlake_ugl = "In-Lake TP (ug/L)",
    chla_ugl      = "Chlorophyll-a (ug/L)",
    secchi_m      = "Secchi Depth (m)",
    tsi_mean      = "Mean Carlson TSI"
  )

  # Keep only metrics that exist in the data and were requested
  metrics <- intersect(metrics, names(scenario_result))
  metrics <- intersect(metrics, names(metric_labels))

  if (length(metrics) == 0)
    stop("None of the requested metrics are present in scenario_result.",
         call. = FALSE)

  # Pivot to long format
  has_target <- "meets_target" %in% names(scenario_result) && highlight_target

  keep_cols <- c("scenario", metrics,
                  if (has_target) "meets_target" else NULL)

  long_df <- scenario_result[, keep_cols, drop = FALSE] |>
    tidyr::pivot_longer(
      cols      = dplyr::all_of(metrics),
      names_to  = "metric",
      values_to = "value"
    ) |>
    dplyr::mutate(
      metric_label = metric_labels[metric],
      metric_label = factor(metric_label, levels = metric_labels[metrics]),
      scenario     = factor(scenario, levels = rev(unique(scenario_result$scenario)))
    )

  if (has_target) {
    long_df <- long_df |>
      dplyr::mutate(
        point_color = dplyr::if_else(meets_target, "Meets target", "Does not meet target")
      )
  }

  title_str <- if (!is.null(lake_name)) {
    sprintf("%s \u2014 Scenario Comparison", lake_name)
  } else {
    "Scenario Comparison"
  }

  p <- ggplot2::ggplot(
    long_df,
    ggplot2::aes(x = value, y = scenario)
  ) +
    ggplot2::facet_wrap(
      ~ metric_label,
      scales = "free_x",
      ncol   = 2
    )

  if (has_target) {
    p <- p +
      ggplot2::geom_point(
        ggplot2::aes(color = point_color),
        size = 3.5
      ) +
      ggplot2::scale_color_manual(
        values = c("Meets target"        = "#4CAF82",
                   "Does not meet target" = "#1B5E8A"),
        name   = NULL
      )
  } else {
    p <- p +
      ggplot2::geom_point(color = "#1B5E8A", size = 3.5)
  }

  # Highlight baseline row
  baseline_y <- which(levels(long_df$scenario) == "Baseline")
  if (length(baseline_y) > 0) {
    p <- p +
      ggplot2::geom_hline(
        yintercept = baseline_y,
        linetype   = "dashed",
        color      = "grey60",
        linewidth  = 0.5
      )
  }

  p <- p +
    ggplot2::labs(
      title   = title_str,
      x       = NULL,
      y       = NULL,
      caption = "okBATHTUB R package"
    ) +
    ok_theme(legend_position = "top")

  p
}


