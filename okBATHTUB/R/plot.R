#' Oklahoma water-themed ggplot2 theme
#'
#' @description
#' A clean, minimal ggplot2 theme used consistently across all okBATHTUB
#' visualization functions. Based on \code{theme_minimal()} with clean
#' typography and grid settings.
#'
#' @param base_size Numeric. Base font size. Default 11.
#' @param legend_position Character. Legend position. Default \code{"bottom"}.
#'
#' @return A \code{ggplot2::theme} object.
#' @export
ok_theme <- function(base_size = 11, legend_position = "bottom") {
  ggplot2::theme_minimal(base_size = base_size) +
  ggplot2::theme(
    plot.title         = ggplot2::element_text(face = "bold", size = base_size + 1),
    plot.subtitle      = ggplot2::element_text(color = "grey40", size = base_size - 1),
    plot.caption       = ggplot2::element_text(color = "grey55", size = base_size - 2,
                                               hjust = 0),
    axis.title         = ggplot2::element_text(size = base_size - 0.5),
    panel.grid.minor   = ggplot2::element_blank(),
    panel.grid.major.x = ggplot2::element_blank(),
    legend.position    = legend_position,
    legend.title       = ggplot2::element_text(face = "bold", size = base_size - 1),
    strip.text         = ggplot2::element_text(face = "bold")
  )
}


# =============================================================================
# 1. ok_plot_response() - Load-response curve
# =============================================================================

#' Plot a load-response curve for a reservoir
#'
#' @description
#' \code{ok_plot_response()} generates a load-response curve showing how
#' predicted in-lake chlorophyll-a, Secchi depth, or mean TSI responds to
#' a range of inflow total phosphorus concentrations. This is the core
#' planning tool for answering "what inflow TP concentration achieves
#' a given trophic state target?"
#'
#' The curve is generated by running the full okBATHTUB pipeline across a
#' sequence of TP concentrations while holding all other parameters constant
#' at the baseline values.
#'
#' @param baseline An \code{okBATHTUB} object that has been run through at
#'   least \code{ok_hydraulics()}. Morphometry and coefficient set are
#'   taken from this object.
#' @param response One of \code{"chla"} (chlorophyll-a, ug/L),
#'   \code{"secchi"} (Secchi depth, m), \code{"tsi"} (mean Carlson TSI),
#'   or \code{"tp_inlake"} (in-lake TP, ug/L). Default \code{"chla"}.
#' @param tp_range Numeric vector of length 2. Range of inflow TP
#'   concentrations to plot (ug/L). Default \code{c(10, 300)}.
#' @param n_points Integer. Number of points along the curve. Default 100.
#' @param target_tsi Numeric. Optional horizontal reference line showing a
#'   TSI target. Only shown when \code{response = "tsi"}.
#' @param target_class Character. Trophic class target. Sets
#'   \code{target_tsi} automatically. One of \code{"oligotrophic"},
#'   \code{"mesotrophic"}, \code{"eutrophic"}.
#' @param show_trophic_bands Logical. Show coloured trophic state background
#'   bands on TSI plots. Default \code{TRUE} when response is \code{"tsi"}.
#' @param current_tp Numeric. Optional vertical reference line showing the
#'   current inflow TP concentration.
#' @param lake_name Character. Lake name for plot title. Default \code{NULL}.
#'
#' @return A \code{ggplot2} object.
#'
#' @examples
#' \dontrun{
#' baseline <- ok_load(
#'   inflow_m3yr   = 45e6,
#'   tp_inflow_ugl = 120,
#'   coefficients  = "oklahoma",
#'   ecoregion     = "Cross Timbers"
#' ) |>
#' ok_hydraulics(surface_area_ha = 890, mean_depth_m = 4.2)
#'
#' ok_plot_response(baseline, response = "tsi",
#'                  target_class = "mesotrophic",
#'                  current_tp   = 120,
#'                  lake_name    = "Arcadia Lake")
#' }
#'
#' @seealso \code{\link{ok_scenario}}, \code{\link{ok_plot_scenario}}
#' @export

ok_plot_response <- function(baseline,
                              response           = c("chla", "secchi",
                                                     "tsi", "tp_inlake"),
                              tp_range           = c(10, 300),
                              n_points           = 100L,
                              target_tsi         = NULL,
                              target_class       = NULL,
                              show_trophic_bands = NULL,
                              current_tp         = NULL,
                              lake_name          = NULL) {

  if (!requireNamespace("ggplot2", quietly = TRUE))
    stop("Package 'ggplot2' is required. Install with: install.packages('ggplot2')",
         call. = FALSE)

  assert_okBATHTUB(baseline)
  response <- match.arg(response)

  if (!is.null(target_class) && is.null(target_tsi)) {
    target_tsi <- switch(tolower(target_class),
                          "oligotrophic" = 40, "mesotrophic" = 50,
                          "eutrophic"    = 70, NULL)
  }

  if (is.null(show_trophic_bands))
    show_trophic_bands <- (response == "tsi")

  d    <- baseline$data
  meta <- baseline$meta

  # Build TP sequence
  tp_seq <- seq(tp_range[1], tp_range[2], length.out = n_points)

  # Run pipeline for each TP value
  curve_df <- do.call(rbind, lapply(tp_seq, function(tp) {
    r <- ok_load(
      inflow_m3yr   = d$inflow_m3yr,
      tp_inflow_ugl = tp,
      tn_inflow_ugl = d$tn_inflow_ugl,
      coefficients  = meta$coefficients %||% "walker",
      ecoregion     = meta$ecoregion
    ) |>
    ok_hydraulics(
      surface_area_ha = d$surface_area_ha,
      mean_depth_m    = d$mean_depth_m,
      outflow_m3yr    = d$outflow_m3yr
    ) |>
    ok_retention() |>
    ok_inlake()    |>
    ok_tsi()

    data.frame(
      tp_inflow  = tp,
      tp_inlake  = r$data$tp_inlake_ugl  %||% NA_real_,
      chla       = r$data$chla_ugl        %||% NA_real_,
      secchi     = r$data$secchi_m        %||% NA_real_,
      tsi        = r$data$tsi_mean        %||% NA_real_,
      tsi_tp     = r$data$tsi_tp          %||% NA_real_,
      state      = r$data$trophic_state   %||% NA_character_
    )
  }))

  # Y-axis setup
  y_col   <- switch(response,
                     chla       = "chla",
                     secchi     = "secchi",
                     tsi        = "tsi",
                     tp_inlake  = "tp_inlake")
  y_label <- switch(response,
                     chla       = "Predicted Chlorophyll-a (ug/L)",
                     secchi     = "Predicted Secchi Depth (m)",
                     tsi        = "Mean Carlson TSI",
                     tp_inlake  = "Predicted In-Lake TP (ug/L)")

  pal <- ok_trophic_colors()

  title_str <- if (!is.null(lake_name)) {
    sprintf("%s \u2014 Load-Response Curve", lake_name)
  } else {
    "Load-Response Curve"
  }

  coeff_label <- if (!is.null(meta$coefficients)) meta$coefficients else "walker"
  eco_label   <- if (!is.null(meta$ecoregion)) meta$ecoregion else "statewide"

  subtitle_str <- sprintf(
    "Coefficients: %s | Ecoregion: %s | HRT: %.2f yr | Area: %.0f ha",
    coeff_label, eco_label,
    d$hydraulic_residence_time_yr %||% NA,
    d$surface_area_ha %||% NA
  )

  p <- ggplot2::ggplot(curve_df,
                        ggplot2::aes(x = tp_inflow, y = .data[[y_col]]))

  # Trophic state bands (TSI only)
  if (show_trophic_bands && response == "tsi") {
    band_df <- data.frame(
      ymin  = c(0,  40, 50, 70),
      ymax  = c(40, 50, 70, 100),
      state = c("Oligotrophic", "Mesotrophic", "Eutrophic", "Hypereutrophic")
    )
    p <- p +
      ggplot2::geom_rect(
        data        = band_df,
        ggplot2::aes(xmin = -Inf, xmax = Inf,
                     ymin = ymin,  ymax = ymax,
                     fill = state),
        alpha       = 0.12,
        inherit.aes = FALSE
      ) +
      ggplot2::scale_fill_manual(
        values = pal,
        name   = "Trophic State",
        guide  = ggplot2::guide_legend(order = 2)
      )
  }

  # Main response curve
  p <- p +
    ggplot2::geom_line(color = "#1B5E8A", linewidth = 1.1) +
    ggplot2::labs(
      title    = title_str,
      subtitle = subtitle_str,
      x        = "Inflow TP (ug/L)",
      y        = y_label,
      caption  = "okBATHTUB R package"
    ) +
    ok_theme()

  # TSI target line
  if (!is.null(target_tsi) && response == "tsi") {
    target_label <- if (!is.null(target_class)) {
      sprintf("%s target (TSI = %.0f)", tools::toTitleCase(target_class),
              target_tsi)
    } else {
      sprintf("TSI target = %.0f", target_tsi)
    }
    p <- p +
      ggplot2::geom_hline(yintercept = target_tsi,
                           linetype = "dashed", color = "#D9534F",
                           linewidth = 0.8) +
      ggplot2::annotate("text", x = tp_range[1] + diff(tp_range) * 0.02,
                         y = target_tsi + 1.5, label = target_label,
                         hjust = 0, size = 3.2, color = "#D9534F")
  }

  # Current TP reference line
  if (!is.null(current_tp)) {
    p <- p +
      ggplot2::geom_vline(xintercept = current_tp,
                           linetype = "dotted", color = "grey30",
                           linewidth = 0.8) +
      ggplot2::annotate("text", x = current_tp + diff(tp_range) * 0.01,
                         y = max(curve_df[[y_col]], na.rm = TRUE) * 0.97,
                         label = sprintf("Current\nTP = %.0f ug/L",
                                         current_tp),
                         hjust = 0, size = 3.0, color = "grey30")
  }

  p
}


